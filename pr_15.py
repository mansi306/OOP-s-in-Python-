# Polymorphism
# 1) len() function performs different operation according to the input 
# string
print(len("mansi")) # it returns the no of characters
# list
print(len(['ram','sham',1,2,3])) # it returns the no of items 
# dictionary
print(len({'car':'bmw','model':'old','price':10000000})) # it returns the number of keys
# 2) reversed function
emp = ['jay','viru','ram']
company = 'capgemini'
for i in reversed(emp):
    print(i)
print("for list now ")
for i in reversed(company):
    print(i) 


    